    public class Operaciones
    {
        public double AreaCuadrado(double lado)
        {
            return lado * lado;
        }
        public double LongitudCirculo(double radio)
        {
            return 2 * Math.PI * radio;
        }
        public double VolumenCubo(double lado)
        {
            return lado * lado *lado;
        }

    }

